function setLanguage(lang) {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            if (key === 'services.title' || key === 'about.title' || key === 'contact.title') {
                const text = translations[lang][key];
                const firstLetter = text.charAt(0);
                const restOfText = text.slice(1);
                const spanClass = key === 'services.title' ? 'servicios-texto' :
                    key === 'about.title' ? 'about-texto' : 'contact-texto';
                element.innerHTML = `<span class="subrayados">${firstLetter}</span><span class="${spanClass}">${restOfText}</span>`;
            } else if (key === 'projects.title') {
                // Special handling for projects title with gradient effect
                const text = translations[lang][key];
                if (lang === 'en') {
                    element.innerHTML = `<span class="text-gradient">My Web</span> Projects`;
                } else {
                    element.innerHTML = `<span class="text-gradient">Mis Proyectos</span> Web`;
                }
            } else if (element.tagName === 'INPUT' && element.getAttribute('placeholder')) {
                element.placeholder = translations[lang][key];
            } else if (key === 'bomberos.carousel.title') {
                // Preservar el ícono del título del carrusel
                const icon = element.querySelector('i');
                if (icon) {
                    const span = element.querySelector('span');
                    if (span) {
                        span.textContent = translations[lang][key];
                    } else {
                        element.innerHTML = icon.outerHTML + ' ' + translations[lang][key];
                    }
                } else {
                    element.textContent = translations[lang][key];
                }
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    localStorage.setItem('preferredLanguage', lang);
    document.getElementById('currentLang').textContent = lang.toUpperCase();
}

// Evento para cambiar idioma
document.getElementById('languageToggle').addEventListener('click', function () {
    const currentLang = localStorage.getItem('preferredLanguage') || 'en';
    const newLang = currentLang === 'en' ? 'es' : 'en';
    setLanguage(newLang);
});

// Cargar idioma preferido al iniciar
document.addEventListener('DOMContentLoaded', function () {
    const preferredLanguage = localStorage.getItem('preferredLanguage') || 'en';
    setLanguage(preferredLanguage);
});